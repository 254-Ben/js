// In let you can reassign but not redeclare
let petName = "Hal"; // DECLARING
petName = "Spot"; // REASSIGNING

//In var you can reassign and redeclare
var city = "Munich";
var city = "New York";

// In const you cannot reassign and redeclare
const vehicle = "bimmer";
//vehicle = "chevy";
console.log(petName);
console.log(city);
console.log(vehicle);
